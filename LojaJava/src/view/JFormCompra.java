    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        // TODO add your handling code here:
        
        if(!txtQuantidade.getText().equals("")){
            
            int q = Integer.parseInt(txtQuantidade.getText());
            int e = Integer.parseInt(lblEstoque.getText());
            int baixa = e - q;

            if(q > 0 && q < e) {

                Cliente c = new Cliente();
                c.setId(Integer.parseInt(lblIdCliente.getText()));

                Item i = new Item();
                i.setId(Integer.parseInt(lblIdItem.getText()));
                i.setEstoque(e);

                Compra compra = new Compra();
                compra.setCliente(c);
                compra.setItem(i);
                compra.setQtd(q);

                CompraDAO dao = new CompraDAO();
                dao.insert(compra);

                JOptionPane.showMessageDialog(null, "Compra realizada com sucesso!");

                txtQuantidade.setText("0");
                lblEstoque.setText("" + baixa);

                preencherTabela();

            }
            else {
                JOptionPane.showMessageDialog(null, "Quantidade incorreta...");
            }
        }
        else {
            JOptionPane.showMessageDialog(null, "Informe a Quandidade");
        }
        
    }//GEN-LAST:event_btnComprarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        int linha = tbCompra.getSelectedRow();
        
        if(linha > -1){
            Compra compra = new Compra();
            compra.setCodigo(Long.parseLong(tbCompra.getValueAt(linha, 0).toString()));
            
            CompraDAO dao = new CompraDAO();
            dao.delete(compra);
            
            JOptionPane.showMessageDialog(null, "Compra removida da lista!");
            preencherTabela();
            
        } else {
            JOptionPane.showMessageDialog(null, "Seleciona uma linha da tabela!");
        }
        
    }//GEN-LAST:event_btnExcluirActionPerformed

