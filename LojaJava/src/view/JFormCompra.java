    private void txtQuantidadeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtQuantidadeKeyReleased
        // TODO add your handling code here:
        
        
        if(!(txtQuantidade.getText().equals(""))){
            
            int q = Integer.parseInt(txtQuantidade.getText());
            int e = Integer.parseInt(lblEstoque.getText());
            double valor = Double.parseDouble(lblValor.getText());
            
            if(q < e){

                lblTotal.setText("" + ((double)q * valor));
            
            } else {
                
                JOptionPane.showMessageDialog(null, "Quantidade limite é " + (--e));
                txtQuantidade.setText("" + e);
                lblTotal.setText("" + ((double)e * valor));
            }
               
        }
        
    }//GEN-LAST:event_txtQuantidadeKeyReleased
    
    private void preencherTabela(){
        
        DefaultTableModel modelo = (DefaultTableModel) tbCompra.getModel();
        modelo.setNumRows(0);
        
        CompraDAO dao = new CompraDAO();
        
        for(Compra c : dao.selectCompra()){
            
            Object[] dados = {
              c.getCodigo(),
              c.getCliente().getNome(),
              c.getItem().getDescricao(),
              c.getQtd(),
              c.getDataHora()
            };
            
            modelo.addRow(dados);
        }
    }
    
    
