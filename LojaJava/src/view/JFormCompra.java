    private void jcomboClientes(){
        
        ClienteDAO cliente = new ClienteDAO();
        
        for(Cliente c : cliente.selectCliente()){
            cbClientes.addItem(c); // pega o valor do metodo toString()
        }
    }
    
    private void jcomboItens(){
        
        ItemDAO item = new ItemDAO();
        
        for(Item i : item.selectItem()){
            cbItens.addItem(i);
        }
        
    }
    
    
    private void relogioDisplay(){
        
        Timer t = new Timer(1000, new ActionListener(){
            @Override
            public void actionPerformed(ActionEvent ae) {
                Calendar cal = Calendar.getInstance(TimeZone.getTimeZone("Brazil/East"));
                
                String hr = "" + cal.get(Calendar.HOUR_OF_DAY);
                String min = "" + cal.get(Calendar.MINUTE);
                String sec = "" + cal.get(Calendar.SECOND);
                
               Date data = new Date();
               
               DateFormat df = new SimpleDateFormat("dd/MM/yyyy");
               
               
                lblDataHora.setText(df.format(data) + " | " + hr + " : " + min +" : " + sec);
            }
            
        });
        t.start();
    }
    
